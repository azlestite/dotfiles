#!/usr/bin/env bash

set -Ceuo pipefail

source ${HOME}/.local/share/chezmoi/.chezmoitemplates/utils.sh

info "OS: $(os_info)"
warn "warning color output test"

command -v bw >/dev/null 2>&1 && exit

case "$(uname -s)" in
Linux)
    info "Install Bitwarden CLI..."
    if [ "$(uname -m)" = "x86_64" ]; then
        # required packages for installing Bitwarden CLI
        packages=(
            "curl"
            "unzip"
            "wget"
        )

        install=false
        to_install=()

        # Loop through the commands to check if they're installed
        for cmd in "${packages[@]}"; do
            if ! command -v "${cmd}" >/dev/null 2>&1; then
                install=true
                to_install+=("${cmd}")
            fi
        done

        # Install missing packages
        if ${install}; then
            for pkg in "${to_install[@]}"; do
                sudo apt install -y "${pkg}"
            done
        fi

        mkdir -p $HOME/.local/{bin,src}
        # curl -fsSL "https://bitwarden.com/download/?app=cli&platform=linux" -o $HOME/.local/src/bw-linux.zip
        # unzip $HOME/.local/src/bw-linux.zip -d $HOME/.local/bin/
        # chmod +x $HOME/.local/bin/bw
        # rm -rf $HOME/.local/src/bw-linux.zip

        TMPDIR="$(mktemp -d)"
        curl -fsSL "https://bitwarden.com/download/?app=cli&platform=linux" -o "${TMPDIR}/bw.zip"
        unzip -qo "${TMPDIR}/bw.zip" -d "${TMPDIR}"
        sudo install -m 0755 "${TMPDIR}/bw" "/usr/local/bin/bw"

        # export PATH="$HOME/.local/bin:$PATH"
        # source $HOME/.profile
        bw login
        bw sync
        export BW_SESSION="$(bw unlock --raw)"
    fi
    ;;
*)
    echo "unsupported OS"
    exit 1
    ;;
esac
